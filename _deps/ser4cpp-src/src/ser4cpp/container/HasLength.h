#ifndef SER4CPP_HASLENGTH_H
#define SER4CPP_HASLENGTH_H

namespace ser4cpp
{

template <class L>
class HasLength
{
public:
    inline L length() const
    {
        return m_length;
    }

    inline bool is_empty() const
    {
        return m_length == 0;
    }

    inline bool is_not_empty() const
    {
        return m_length != 0;
    }

protected:
    HasLength() {}

    explicit HasLength(L length) : m_length(length)
    {}

    L m_length = 0;
};

}

#endif
